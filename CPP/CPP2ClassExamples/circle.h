class circle
{
    private:
        double radius;
        double pi;
    public:
        //circle();
        circle(double r =0);
        bool setRadius(double r);
        double getRadius();
        double getArea();
        double getDiameter();
        double getCircumfrence();   
};
////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////
double circle::getCircumfrence()
{
    return 2 * pi * radius;
}
///////////////////////////////////////////////////////////////////////
double circle::getDiameter()
{
    return radius * 2;   
}
///////////////////////////////////////////////////////////////////////
double circle::getArea()
{
    return pi * radius * radius;    
}
//////////////////////////////////////////////////////////////////////
double circle::getRadius()
{
    return radius;
}
///////////////////////////////////////////////////////////////////////
bool circle::setRadius(double r)
{
    if(r > 0)
    {
        radius = r;
        return true;
    }

    return false;
}
